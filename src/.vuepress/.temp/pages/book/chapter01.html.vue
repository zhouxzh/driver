<h2 id="挑战" tabindex="-1"><a class="header-anchor" href="#挑战"><span>挑战</span></a></h2>
<p>碳化硅功率器件面临的挑战主要包括缩放的限制因素、与碳化硅器件较小的管芯尺寸相关的散热问题、管芯上与封装相关的应变以及衬底可用性。此外，碳化硅功率器件的制造成本较高，且生产工艺相对复杂。不过，随着技术的不断发展，这些问题将逐渐得到解决。</p>
<p>随着技术的不断发展，碳化硅衬底的尺寸逐渐扩大，成本也会逐渐降低。例如，8英寸SiC衬底将比6英寸在成本降低上具有明显优势。但是目前实际上仅有Wolfspeed实现8英寸碳化硅量产，而大多数国际企业则将8英寸碳化硅衬底的量产节点定在2023年左右。</p>
