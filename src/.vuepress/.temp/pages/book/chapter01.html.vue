<p>SiC（碳化硅）是一种由硅（Si）和碳（C）组成的化合物半导体材料。上面的表格显示SiC具有优异的介电击穿场强度（击穿场）和带隙（能隙），分别比Si大10倍和3倍。因此，作为制作功率半导体器件的材料，SiC可以超越Si。SiC具有各种多晶型（晶体多态性），每种多晶型都显示出不同的物理特性。对于功率器件，4H-SiC被认为是理想的。</p>
<h2 id="应用" tabindex="-1"><a class="header-anchor" href="#应用"><span>应用</span></a></h2>
<p>应用领域分析<br>
材料	核心优势	典型应用场景	局限性<br>
Si	低成本、成熟工艺	集成电路、低压功率器件、传感器	高频/高压性能差，效率低<br>
GaAs	超高电子迁移率	射频前端（手机基站）、激光二极管	击穿场强低，散热差<br>
GaN	高频高功率结合	5G射频放大器、快充电源、雷达	热导率中等，衬底成本高<br>
4H-SiC	高耐压+高导热	电动汽车逆变器、电网变流器、光伏逆变器	晶圆缺陷率较高，成本高于Si<br>
β-Ga₂O₃	超高击穿场强、理论成本低	超高压功率器件（＞10 kV）	热导率极低，工艺尚未成熟<br>
金刚石	极限性能（耐压、导热、迁移率）	航天级器件、核反应堆传感器、高频太赫兹	掺杂困难，晶圆尺寸小，成本极高</p>
<p>SiC的介电击穿场强度比Si高，这允许制造具有比Si器件具有更高杂质浓度和更薄厚度的漂移层的高击穿电压功率器件。这导致具有非常低单位面积导通电阻的高击穿电压器件。与Si相比，漂移层电阻每个面积可以在相同的击穿电压下理论上降低到1/300。使用SiC可以实现同时具有“高击穿电压”，“低导通电阻”和“高速度”的大多数载流子器件（肖特基势垒二极管和MOSFET）。此外，其带隙大约是Si的3倍，使得可以在更高温度下操作功率器件（尽管由于包装的耐热可靠性限制，目前保证温度约为150°C至175°C，但随着包装技术的进步，可以实现保证温度高于200°C）。</p>
<p>碳化硅等宽禁带半导体的应用主要分布在电力电子器件、新能源汽车、光伏、机车牵引和微波通讯器件等领域。碳化硅半导体以其制作的器件具有耐高温、耐高压、高频、大功率、抗辐射等特点，具有开关速度快、效率高的优势，可大幅降低产品功耗、提高能量转换效率并减小产品体积，主要应用于以5G通信、国防军工、航空航天为代表的射频领域和以新能源汽车、“新基建”为代表的电力电子领域，在民用、军用领域均具有可观的市场前景。</p>
